    public void setMemberDetailsToTable(){
                model =(DefaultTableModel) tbl_memberDetails.getModel();
                model.setRowCount(0);
        
        
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_senanayake", "root", "");
            String sql = "SELECT * FROM member_details";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                String memberId = rs.getString("member_id");
                String memberName = rs.getString("member_name");
                String gender = rs.getString("member_gender");
                String memberContact = rs.getString("member_contact");

                Object[] obj = {memberId, memberName, gender, memberContact};

                model.addRow(obj);
            }

            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }
    
    
