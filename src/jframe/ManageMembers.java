    private void txt_member_idActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_member_idActionPerformed

        String memberId = txt_member_id.getText();

        if (memberId.isEmpty()) {
            return;
        }

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_senanayake", "root", "");
            String sql = "SELECT * FROM member_details WHERE member_id = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, memberId);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                txt_member_name.setText(rs.getString("member_name"));
                combo_gender.setSelectedItem(rs.getString("member_gender"));
                txt_member_contact.setText(rs.getString("member_contact"));

                DefaultTableModel model = (DefaultTableModel) tbl_memberDetails.getModel();
                for (int i = 0; i < model.getRowCount(); i++) {
                    if (model.getValueAt(i, 0).toString().equals(memberId)) {
                        tbl_memberDetails.setRowSelectionInterval(i, i);
                        tbl_memberDetails.scrollRectToVisible(tbl_memberDetails.getCellRect(i, 0, true)); // Scroll to the selected row
                        break;
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "No member found with the given ID.", "Error", JOptionPane.ERROR_MESSAGE);
            }

            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }

    }//GEN-LAST:event_txt_member_idActionPerformed

